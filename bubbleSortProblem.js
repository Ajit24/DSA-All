function bubbleSort(arr) {
    var temp;
    var result = "";
    for (var i = 0; i < arr.length - 1; i++) {
        for (var j = 0; j < arr.length - i - 1; j++) {
            if (arr[j] > arr[j + 1]) {
                temp = arr[j];
                arr[j] = arr[j + 1];
                arr[j + 1] = temp;
            }
        }

    }
    return arr.join(" ");
}

function runProgram(input) {
    var newInput = input.trim().split("\n");
    var arr = newInput[1].split(" ").map(Number);
    console.log(bubbleSort(arr));
}
